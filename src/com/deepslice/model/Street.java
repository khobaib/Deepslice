package com.deepslice.model;

public class Street {
	public String SuburbID;
    public String AddressType;
    public String StreetID;
    public String StreetAbbr;
    public String StreetName;
    
    
	public String getSuburbID() {
		return SuburbID;
	}
	public void setSuburbID(String suburbID) {
		SuburbID = suburbID;
	}
	public String getAddressType() {
		return AddressType;
	}
	public void setAddressType(String addressType) {
		AddressType = addressType;
	}
	public String getStreetID() {
		return StreetID;
	}
	public void setStreetID(String streetID) {
		StreetID = streetID;
	}
	public String getStreetAbbr() {
		return StreetAbbr;
	}
	public void setStreetAbbr(String streetAbbr) {
		StreetAbbr = streetAbbr;
	}
	public String getStreetName() {
		return StreetName;
	}
	public void setStreetName(String streetName) {
		StreetName = streetName;
	}
    
}
